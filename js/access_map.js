flg1 = 1;
flg2 = 1;
flg3 = 1;
areaBuf1 = 0;
areaBuf2 = 0;
areaBuf3 = 0;

function infoChange(area, button, state, num) {

  /*   alert(num); */

  if (num == 1) {
    areaBuf = areaBuf1;
    flg = flg1;
  }else if(num == 2) {
    areaBuf = areaBuf2;
    flg = flg2;
  }else {
    areaBuf = areaBuf3;
    flg = flg3;
  }
	flg = (flg == 0) ? 1 : 0;
	if(areaBuf != state) {
		if (flg == 0) {
		//	document.getElementById("areaInfo"+num).innerHTML = area;
			document.getElementById("areaInfo1"+num).innerHTML = "";
			document.getElementById("areaInfo"+num).className = "areaInfo fadeIn";
			document.getElementById("areaInfo1"+num).className = "areaInfo fadeOut";
			document.getElementById(button).checked = true;
		} else {
		//	document.getElementById("areaInfo1"+num).innerHTML = area;
			document.getElementById("areaInfo"+num).innerHTML = "";
			document.getElementById("areaInfo"+num).className = "areaInfo fadeOut";
			document.getElementById("areaInfo1"+num).className = "areaInfo fadeIn";
			document.getElementById(button).checked = true;
		}
	}
	areaBuf = state;
  if (num == 1) {
    areaBuf1 = areaBuf;
    flg1 = flg;
  }else if(num == 2) {
    areaBuf2 = areaBuf;
    flg2 = flg;
  }else {
    areaBuf3 = areaBuf;
    flg3 = flg;
  }
}


function infoHover(button) {
  document.getElementById(button).checked = true;
}

function infoOut(button) {
  document.getElementById(button).checked = false;
}
